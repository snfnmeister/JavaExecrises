/*
 *
 * Java For Loop
 * syntax:  for (initialization; termination;
 *               increment) {
 *               statement(s)
 *           }
 */

public class ForLoop {

    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.println("i is equal to " + i);
        }

    }
}
