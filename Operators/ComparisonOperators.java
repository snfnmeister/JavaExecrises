/*
 *
 * Java Comparison Operators
 *
 */

class ComparisonOperators {

    public static void main(String[] args) {

        int a = 10;
        int b = 20;

        System.out.println("Оператор '==' - для определения равенства: a == b? Ответ: " + (a == b));
        System.out.println("Оператор '!=' - для определения неравенства: a != b? Ответ: " + (a != b));
        System.out.println("Оператор '>' -  определение a больше b: a > b? Ответ: " + (a > b));
        System.out.println("Оператор '<' -  определение a меньше b: a < b? Ответ: " + (a < b));
        System.out.println("Оператор '>=' -  определение a больше или равно b: a >= b? Ответ: " + (a >= b));
        System.out.println("Оператор '<=' -  определение a меньше или равно b: a <= b? Ответ: " + (a <= b));

    }


}
