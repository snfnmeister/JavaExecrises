/*
 *
 * Java instanceof operator - this operator checks object for accordance to (class/type interface) that pointed
 * syntax: (variable referenced object) instanceof (class/type interface) - returns true of false value
 *
 */

public class InstanceOf {
    public static void main(String[] args) {
        String JavaStr = "Java";
        boolean result = JavaStr instanceof String;
        System.out.println("JavaStr is String type?: - " + result);

    }
}
